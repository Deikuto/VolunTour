export type TranslationKey = keyof typeof translations['bg']

export function translate(lang: Lang, key: TranslationKey, vars?: Record<string, string | number>): string {
  let str: string = translations[lang][key] ?? translations.bg[key] ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{${k}}`, String(v))
    }
  }
  return str
}
